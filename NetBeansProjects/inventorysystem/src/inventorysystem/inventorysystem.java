    private void txtdrugcodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtdrugcodeKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER)
        {
            try {
                String drugcode = txtdrugcode.getText();
                pst = con.prepareStatement(" select * from product where id = ?");
                pst.setString(1, drugcode);
                rs = pst.executeQuery();
                
                if(rs.next() == false)
                {
                JOptionPane.showMessageDialog(this, "Drug Code Not Found");
                }
                else
                {
                    String dname = rs.getString("drugname");
                    txtdrugname.setText(dname.trim());
                    
                    String price = rs.getString("price");
                    txtprice.setText(price.trim());
                    
                    txtquantity.requestFocus();
                    
                }
                
                
            } catch (SQLException ex) {
                Logger.getLogger(inventorysystem.class.getName()).log(Level.SEVERE, null, ex);
            }
        }    
        
        
        
    }//GEN-LAST:event_txtdrugcodeKeyPressed

    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
        // TODO add your handling code here:
        int price,quantity,total;
        
        price = Integer.parseInt(txtprice.getText());
        quantity = Integer.parseInt(txtquantity.getValue() .toString());
        
        total = price * quantity;
        
        
        
        df = (DefaultTableModel)jTable1.getModel();
        df.addRow(new Object[]
        {
            txtdrugname.getText(),
            txtprice.getText(),
            txtquantity.getValue().toString(),
            total
            
        });
        
        int sum = 0;
        
        for (int i=0; i<jTable1.getRowCount(); i++)
        {
            sum = sum + Integer.parseInt(jTable1.getValueAt(i, 3) .toString());
        }
        
        txttotalcost.setText(String.valueOf(sum));
        txtdrugcode.setText("");
        txtdrugname.setText("");
        txtquantity.setValue(0);
        txtprice.setText("");
        
        txtdrugcode.requestFocus();
        
        
        sales();
    }//GEN-LAST:event_addbtnActionPerformed

