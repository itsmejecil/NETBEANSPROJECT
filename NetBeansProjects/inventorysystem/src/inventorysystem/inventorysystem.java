    public void sales()
    {
        String totalcost = txttotalcost.getText();
         String payment = txtpayment.getText();
          String balance = txtbalance.getText();
          
          
          
          int sales_id = 0;
          
          
        try {
            String query = "insert into sales(subtotal,payment,balance)values(?,?,?)";
            pst = con.prepareStatement(query,Statement.RETURN_GENERATED_KEYS);
            pst.setString(1, totalcost);
            pst.setString(2, payment);
            pst.setString(3, balance);
            pst.executeUpdate();
            rs = pst.getGeneratedKeys();
            
            if(rs.next())
            {
                sales_id = rs.getInt(1);  
            }
            
            int rows = jTable1.getRowCount();
            
            
             String query1 = "insert into sales_product(sales_id,drugname,price,quantity,total)values(?,?,?,?,?)";
            pst1 = con.prepareStatement(query1);
            
            String drugname = "";
            String price;
            String quantity;
            int total = 0;
            
            for(int i =0; i<jTable1.getRowCount(); i++)
            {
                drugname = (String)jTable1.getValueAt(i, 0);
                price = (String)jTable1.getValueAt(i, 1);
                quantity = (String)jTable1.getValueAt(i, 2);
                total = (int)jTable1.getValueAt(i, 3);
                
                
                pst1.setInt(1, sales_id);
                pst1.setString(2, drugname);
                pst1.setString(3, price);
                pst1.setString(4, quantity);
                pst1.setInt(5, total);
            }
            
            JOptionPane.showMessageDialog(this, "Sales Compleeeeeeted");
            
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(inventorysystem.class.getName()).log(Level.SEVERE, null, ex);
        }
          
    
    
    
    
    }
  
